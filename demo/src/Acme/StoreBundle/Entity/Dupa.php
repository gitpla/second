<?php

namespace Acme\StoreBundle\Entity;

/**
 * Dupa
 */
class Dupa
{
    /**
     * @var string
     */
    private $name;

    /**
     * @var integer
     */
    private $id;

    /**
     * @var \Acme\StoreBundle\Entity\User
     */
    private $user;


    /**
     * Set name
     *
     * @param string $name
     *
     * @return Dupa
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set user
     *
     * @param \Acme\StoreBundle\Entity\User $user
     *
     * @return Dupa
     */
    public function setUser(\Acme\StoreBundle\Entity\User $user = null)
    {
        $this->user = $user;

        return $this;
    }

    /**
     * Get user
     *
     * @return \Acme\StoreBundle\Entity\User
     */
    public function getUser()
    {
        return $this->user;
    }
}
